"""### Analyzing Positive Features

Let's delve into the content of positive reviews to understand what aspects are frequently praised by users.
"""

from nltk.corpus import stopwords
from collections import Counter
import re

# Download stopwords if not already downloaded
try:
    nltk.data.find('corpora/stopwords')
except LookupError:
    nltk.download('stopwords')

stop_words = set(stopwords.words('english'))

# Filter for positive reviews
positive_reviews_df = df[df['sentiment_label'] == 'Positive']

# Combine all positive review texts into a single string
all_positive_text = ' '.join(positive_reviews_df['review'].tolist())

# Preprocess the text:
# 1. Convert to lowercase
# 2. Remove punctuation and numbers
# 3. Tokenize words
# 4. Remove stop words

cleaned_text = re.sub(r'[^a-zA-Z\s]', '', all_positive_text.lower())
words = cleaned_text.split()
filtered_words = [word for word in words if word not in stop_words and len(word) > 2]

# Count word frequencies
word_counts = Counter(filtered_words)

# Get the 10 most common words
top_positive_words = word_counts.most_common(10)

print("Top 10 most common words in positive reviews:")
for word, count in top_positive_words:
    print(f"{word}: {count}")

import matplotlib.pyplot as plt
import seaborn as sns

# Prepare data for plotting
words, counts = zip(*top_positive_words)

plt.figure(figsize=(12, 8))
sns.barplot(x=list(counts), y=list(words), palette='viridis')
plt.title('Top 10 Most Common Words in Positive Reviews')
plt.xlabel('Frequency')
plt.ylabel('Words')
plt.show()

