"""Sentiment Analysis"""

import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
from collections import Counter
import re

# Import NLTK and initialize the SentimentIntensityAnalyzer if not already done in the previous step
import nltk
from nltk.sentiment.vader import SentimentIntensityAnalyzer

# Download the VADER lexicon (if not already downloaded)
try:
    nltk.data.find('sentiment/vader_lexicon.zip')
except LookupError:
    nltk.download('vader_lexicon')

sia = SentimentIntensityAnalyzer()

# Fill any NaN values in the 'review' column with an empty string to avoid errors during sentiment analysis
df['review'] = df['review'].fillna('')

# Apply sentiment analysis to the 'review' column
df['sentiment'] = df['review'].apply(sia.polarity_scores)

# Extract compound score and other scores into separate columns
df['compound_score'] = df['sentiment'].apply(lambda x: x['compound'])
df['pos'] = df['sentiment'].apply(lambda x: x['pos'])
df['neg'] = df['sentiment'].apply(lambda x: x['neg'])
df['neu'] = df['sentiment'].apply(lambda x: x['neu'])

# Classify sentiment based on compound score
def classify_sentiment(score):
    if score >= 0.05:
        return 'Positive'
    elif score <= -0.05:
        return 'Negative'
    else:
        return 'Neutral'

df['sentiment_label'] = df['compound_score'].apply(classify_sentiment)

# Display the first few rows with the new sentiment columns
print("DataFrame with sentiment scores and labels:")
display(df[['review', 'ratings', 'compound_score', 'pos', 'neg', 'neu', 'sentiment_label']].head())

