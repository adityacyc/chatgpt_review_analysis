"""### Review Evolution Over Time

Let's examine how sentiment and ratings have evolved over the review period.
"""

# Aggregate data by month to see trends
monthly_sentiment = df.groupby(df['review_date'].dt.to_period('M')).agg(
    avg_compound_score=('compound_score', 'mean'),
    positive_count=('sentiment_label', lambda x: (x == 'Positive').sum()),
    neutral_count=('sentiment_label', lambda x: (x == 'Neutral').sum()),
    negative_count=('sentiment_label', lambda x: (x == 'Negative').sum()),
    total_reviews=('sentiment_label', 'count')
).reset_index()

# Convert the 'review_date' (which is now Period objects) to datetime for plotting
monthly_sentiment['review_date'] = monthly_sentiment['review_date'].dt.to_timestamp()

# Calculate proportions
monthly_sentiment['positive_proportion'] = monthly_sentiment['positive_count'] / monthly_sentiment['total_reviews']
monthly_sentiment['neutral_proportion'] = monthly_sentiment['neutral_count'] / monthly_sentiment['total_reviews']
monthly_sentiment['negative_proportion'] = monthly_sentiment['negative_count'] / monthly_sentiment['total_reviews']

display(monthly_sentiment.head())

plt.figure(figsize=(15, 7))
sns.lineplot(data=monthly_sentiment, x='review_date', y='avg_compound_score', marker='o')
plt.title('Average Compound Sentiment Score Over Time (Monthly)')
plt.xlabel('Date')
plt.ylabel('Average Compound Score')
plt.grid(True)
plt.tight_layout()
plt.show()

plt.figure(figsize=(15, 7))
sns.lineplot(data=monthly_sentiment, x='review_date', y='positive_proportion', label='Positive', marker='o')
sns.lineplot(data=monthly_sentiment, x='review_date', y='neutral_proportion', label='Neutral', marker='o')
sns.lineplot(data=monthly_sentiment, x='review_date', y='negative_proportion', label='Negative', marker='o')
plt.title('Proportion of Sentiment Labels Over Time (Monthly)')
plt.xlabel('Date')
plt.ylabel('Proportion')
plt.legend()
plt.grid(True)
plt.tight_layout()
plt.show()